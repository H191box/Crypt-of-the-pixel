/**
 * Generate a proper isometric diamond tile and load all tiles to VRAM.
 */
void load_all_tiles(void) {
    /* VRAM char block 0 starts at VRAM + 0x0000 */
    /* Each tile = 32 bytes. We load tile data for char block 0. */
    /* BG0 uses char block 0, BG1 uses char block 1 (offset 0x4000). */
    
    /* Load normal tiles to BG char block 0 */
    for (int i = 0; i < NUM_TILE_TYPES; i++) {
        void* dst = (void*)(VRAM + (i * 2) * GBA_TILE_BYTES);
        dma_to_vram(dst, tile_ptrs[i], ISO_TILE_BYTES);
    }
    
    /* Load dark variants to BG char block 1 */
    /* Dark variant: shift palette indices to darker colors */
    /* For simplicity, we just copy the same tiles with a palette remap */
    for (int i = 0; i < NUM_TILE_TYPES; i++) {
        void* dst = (void*)(VRAM + 0x4000 + (i * 2) * GBA_TILE_BYTES);
        dma_to_vram(dst, tile_ptrs[i], ISO_TILE_BYTES);
    }
    
    /* Generate proper isometric floor tiles at runtime */
    generate_isometric_tiles();
}

/**
 * Generate isometric diamond tiles programmatically.
 * Creates proper transparent-edge diamond shapes in VRAM.
 */
static void generate_isometric_tiles(void) {
    /* Generate 3 floor tile variants with proper diamond shape */
    u8 tile_buf[32];  /* One 8×8 4bpp tile */
    
    for (int variant = 0; variant < 3; variant++) {
        for (int half = 0; half < 2; half++) {
            /* Clear buffer */
            for (int i = 0; i < 32; i++) tile_buf[i] = 0;
            
            /* Generate diamond pixels */
            for (int y = 0; y < 8; y++) {
                /* Diamond: at row y, the leftmost non-transparent pixel
                 * is at column (4 - y/2) for left half, mirrored for right.
                 * Actually for a 16×8 diamond in 2 halves:
                 * Left half: pixels fill from center-right to left
                 * Right half: pixels fill from center-left to right
                 */
                
                for (int x = 0; x < 8; x++) {
                    int full_x = half * 8 + x;  /* 0-15 */
                    
                    /* Diamond check: |full_x - 7.5| + |y - 3.5| <= 4 */
                    int dx = ABS(full_x * 2 - 15);  /* Scale to avoid float */
                    int dy = ABS(y * 2 - 7);
                    
                    if (dx + dy <= 8) {
                        /* Inside diamond — pick stone color */
                        u8 color;
                        int pattern = (x + y + variant * 3) % 7;
                        switch (pattern) {
                            case 0: color = 1; break;  /* dark stone */
                            case 1: color = 3; break;  /* medium */
                            case 2: color = 8; break;  /* highlight */
                            case 3: color = 2; break;  /* mortar */
                            default: color = 1; break;
                        }
                        
                        /* Write 4bpp pixel at (x, y) in the 8×8 tile */
                        int byte_idx = y * 4 + x / 2;
                        if (x % 2 == 0) {
                            tile_buf[byte_idx] = (color << 4) | (tile_buf[byte_idx] & 0x0F);
                        } else {
                            tile_buf[byte_idx] = (tile_buf[byte_idx] & 0xF0) | color;
                        }
                    }
                }
            }
            
            /* Write to VRAM */
            u16 tile_idx = (1 + variant) * 2 + half;  /* Floor tiles start at index 2 */
            void* dst = (void*)(VRAM + tile_idx * GBA_TILE_BYTES);
            dma_to_vram(dst, tile_buf, 32);
        }
    }
}
