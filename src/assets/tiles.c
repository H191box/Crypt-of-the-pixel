/**
 * tiles.c — Isometric Tile Graphics Data (4bpp)
 * 
 * Each isometric tile is 16×8 pixels stored as 2 consecutive 8×8 GBA tiles
 * in 4bpp format (32 bytes per 8×8 tile).
 * 
 * 4bpp encoding: Each byte contains 2 pixels (high nibble = left pixel, low = right).
 * Color index 0 = transparent.
 * 
 * The isometric diamond shape is created by using color 0 (transparent) for
 * pixels outside the diamond boundary.
 */

