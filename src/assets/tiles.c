/*
 * Floor Stone Tile (16×8 isometric diamond)
 * Palette indices: 0=transparent, 1=dark stone, 2=crack line, 3=medium stone
 * 
 * Row layout (16 pixels wide):
 * 0:                  1 (1 pixel)
 * 1:                1   1 (2 pixels)
 * 2:              1 2   2 1 (4 pixels)
 * 3:            1 3 1   1 3 1 (6 pixels)
 * 4:          1 2 3 1   1 3 2 1 (8 pixels)
 * 5:        1 3 1 2 3   3 2 1 3 1 (10 pixels)
 * 6:      1 2 1 3 1 2   2 1 3 1 2 1 (12 pixels)
 * 7:    1 3 1 2 1 3 1   1 3 1 2 1 3 1 (14 pixels)
 * 8:  1 2 1 3 1 2 1 3   3 1 2 1 3 1 2 1 (16 pixels)
 * ... mirror for bottom half (rows 9-15 not needed since tile is only 8 tall)
 * 
 * Actually our iso tile is 16 wide × 8 tall. The diamond spans the full height.
 */

