/* Expose the raw 1bpp font data as the public symbol */
const u8 font_data[FONT_DATA_SIZE] = {0};  /* Not used directly (raw_font used internally) */

/* ---- 4bpp font tile storage ---- */
/* This is writable so we can convert at runtime */
static u8 s_font_4bpp[FONT_CHAR_COUNT * 32];

/**
 * Convert 1bpp font to 4bpp and return pointer.
 * Each 1bpp char (8 bytes) -> 4bpp tile (32 bytes).
 */
const u8* font_init_4bpp(void) {
    for (int c = 0; c < FONT_CHAR_COUNT; c++) {
        for (int row = 0; row < 8; row++) {
            u8 bitmap = raw_font[c * 8 + row];
            for (int px = 0; px < 4; px++) {
                u8 left_bit  = (bitmap & (0x80 >> (px * 2))) ? 1 : 0;
                u8 right_bit = (bitmap & (0x40 >> (px * 2))) ? 1 : 0;
                s_font_4bpp[c * 32 + row * 4 + px] = (left_bit << 4) | right_bit;
            }
        }
    }
    return s_font_4bpp;
}

/* Public symbol — points to generated data after font_init_4bpp() is called.
 * We alias the static buffer to this symbol by making it a pointer. */
/* Actually, font_tiles is declared as const u8[] in font.h. 
 * We change font.h to declare it as extern const u8* instead.
 * For now, we provide a compatible definition: */

/* The text_renderer uses font_tiles as extern const u8 font_tiles[].
 * We can't easily alias, so we modify text_renderer to call font_init_4bpp
 * and use the returned pointer instead. */

/**
 * Initialize font system and get 4bpp tile data.
 * Call once during startup. Returns pointer to 95×32 byte tile array.
 */
const u8* font_get_tiles(void) {
    static const u8* cached = NULL;
    if (!cached) {
        cached = font_init_4bpp();
    }
    return cached;
}
